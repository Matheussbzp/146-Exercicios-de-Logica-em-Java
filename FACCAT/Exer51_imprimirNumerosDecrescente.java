// 51) Escreva um algoritmo para imprimir os nÃºmeros de 1 (inclusive) a 10 (inclusive) em ordem 
// decrescente.

public class Exer51_imprimirNumerosDecrescente {
    public static void main(String[] args) {
        for (int i = 10; i >= 1; i--) {
            System.out.print(i + " ");
        }
        System.out.println();
    }
}
